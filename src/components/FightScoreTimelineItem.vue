<template>
  <v-timeline-item :dot-color="scorerColor">
    <template v-slot:opposite>
      <v-menu>
        <template v-slot:activator="{ props }">
          <v-btn
            class="mx-2"
            variant="plain"
            v-bind="props"
            icon
          >
            <v-icon color="grey" icon="mdi-dots-horizontal"/>
          </v-btn>
        </template>

        <v-list class="border-thin rounded-lg">
          <v-list-item @click="">
            <template v-slot:title>
              <v-icon icon="mdi-pencil" class="mr-2"/>
              Editar Score
            </template>
          </v-list-item>

          <v-list-item @click="deleteScore()">
            <template v-slot:title>
              <v-icon icon="mdi-trash-can" class="mr-2"/>
              Eliminar Score
            </template>
          </v-list-item>
        </v-list>
      </v-menu>

      <span>{{score.createdDate?.hour}}:{{score.createdDate?.minute}}</span>
    </template>
    
    <v-card variant="flat" class="border-md">
      <template v-slot:text>
        <div class="d-flex">
          <strong class="me-4">{{score.verdict}} </strong>

          <div v-if="isScorable">
            <strong>
              {{score.scorer?.name}}
            </strong>

            <v-divider class="my-2"/> 

            <div class="text-caption" v-if="score.afterblow">
              Afterblow
            </div>

            <div class="text-caption" v-if="score.control">
              Control
            </div>
          </div>

        </div>

        <div class="d-flex" v-if="false">
          <div>
            <strong>{{score.verdict}} 
              <template v-if="isScorable">
                - {{score.scorer?.name}}
              </template>
            </strong>
            
            <div class="text-caption">
              {{score.type}}
            </div>

            <div class="text-caption" v-if="score.afterblow">
              Afterblow
            </div>

            <div class="text-caption text-grey" v-if="score.warnings">
              <v-icon icon="mdi-alert"/> Rojo - {{score.warnings}}
            </div>
          </div>
        </div>
      </template>
    </v-card>
  </v-timeline-item>
</template>

<script lang="ts">
import Match from '@/models/Match';
import MatchScore, { Verdict } from '@/models/MatchScore';
import MatchScoreService from '@/services/MatchScoreService';

export default defineComponent({
  props: {
    match: { type: Match, required: true },
    score: { type: MatchScore, required: true },
  },

  computed: {
    verdict() {
      return Verdict;
    },
    isScorable() {
      return (this.score.verdict != this.verdict.DOUBLE) && (this.score.verdict != this.verdict.NO_EXCHANGE);
    },

    scorerColor(): string {
      if (this.score.scorerId == this.match.fencer_1_id) {
        return "red-darken-3"
      }

      if (this.score.scorerId == this.match.fencer_2_id) {
        return "grey-darken-4"
      }

      return "grey-darken-3"
    }
  },

  methods: {
    async deleteScore() {
      await MatchScoreService.delete(this, this.score.id!)
      this.$emit('onDeletedScore')
    },
  },
})
</script>